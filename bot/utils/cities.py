def normalize_city(city: str) -> str | None:
    """
    Проверяет город по списку.
    Возвращает нормализованное название (с заглавной буквы) или None если не найден.
    """
    cleaned = city.strip().lower()

    # Прямое совпадение
    if cleaned in RUSSIAN_CITIES:
        return city.strip().title()

    # Проверка на опечатки — ищем похожий город (расстояние Левенштейна)
    best_match = None
    best_dist = 999

    for known in RUSSIAN_CITIES:
        dist = _levenshtein(cleaned, known)
        if dist < best_dist:
            best_dist = dist
            best_match = known

    # Допускаем 1 ошибку для коротких городов, 2 для длинных
    max_allowed = 2 if len(cleaned) <= 6 else 4
    if best_dist <= max_allowed:
        return best_match.title()

    return None

