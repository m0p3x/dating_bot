def _levenshtein(a: str, b: str) -> int:
    """Вычисляет расстояние Левенштейна между двумя строками."""
    if abs(len(a) - len(b)) > 3:
        return 999
    dp = list(range(len(b) + 1))
    for i, ca in enumerate(a):
        new_dp = [i + 1]
        for j, cb in enumerate(b):
            new_dp.append(min(
                dp[j + 1] + 1,
                new_dp[j] + 1,
                dp[j] + (0 if ca == cb else 1),
            ))
        dp = new_dp
    return dp[len(b)]